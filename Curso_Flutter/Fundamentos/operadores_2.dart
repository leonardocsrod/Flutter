main() {
  // Operadores de atribuição(binários/infix)
  double a = 2;
  print(a);
  a = a + 3;
  print(a);
  a += 3;
  print(a);
  a -= 3;
  print(a);
  a *= 3;
  print(a);
  a /= 3;
  print(a);
  a %= 3;
  print(a);

  // Operadores relacionais (binários/infix) => o resultado é bool
  print(3 > 2);
  print(3 >= 3);
  print(3 < 3);
  print(3 != 3);
  print(3 == 3);
  print(3 == '3');
  print(2 + 5 > 3 && 4 + 7 != 7 - 4);

  
}
